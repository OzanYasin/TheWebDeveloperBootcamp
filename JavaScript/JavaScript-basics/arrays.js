// MORE ARRAY METHODS

// Array.prototype.concat(); The concat() method is used to merge two or more arrays. This method does not change the existing arrays, but instead returns a new array.

// Array.prototype.includes(); determines whether an array includes a certain value among its entries, returning true or false as appropriate.

// Array.prototype.indexOf(); The indexOf() method returns the first index at which a given element can be found in the array, or -1 if it is not present.

// Array.prototype.reverse(); The reverse() method reverses an array in place. The first array element becomes the last, and the last array element becomes the first.

// ---------------------------------

// MORE MORE ARRAY METHODS

// Array.prototype.slice(); The slice() method returns a shallow copy of a portion of an array into a new array object selected from start to end (end not included) where start and end represent the index of items in that array. The original array will not be modified.

// Array.prototype.splice(); The splice() method changes the contents of an array by removing or replacing existing elements and/or adding new elements in place. To access part of an array without modifying it.

// Array.prototype.sort(); The sort() method sorts the elements of an array in place and returns the sorted array. The default sort order is ascending, built upon converting the elements into strings, then comparing their sequences of UTF-16 code units values. ** Not commonly used.

